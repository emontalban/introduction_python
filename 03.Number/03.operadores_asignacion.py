
# += (Suma y asignación)
suma = 10
suma += 3 
suma = suma + 3
print(suma)
#-= (Resta y asignación)
resta =10
resta -= 2  
resta = resta - 2
print(resta)
# *= (Multiplicación y asignación)
multiplicacion = 10
multiplicacion *= 4 
multiplicacion = multiplicacion * 4
print(multiplicacion)
# /= (División y asignación) 
division =10
division /= 2  
division = division / 2
print(division)
# //= (División entera y asignación) 
division_floor = 10
division_floor //= 3  
division_floor = division_floor // 3
print(division_floor)
# %= (Módulo y asignación)
modulo = 10
modulo %= 2  
modulo = modulo % 2
print(modulo)
# **= (Exponente y asignación)
exponente = 10
exponente **= 3  
exponente = exponente ** 3
print(exponente)